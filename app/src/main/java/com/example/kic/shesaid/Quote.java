package com.example.kic.shesaid;


public class Quote {

    private int mImage;
    private int mQuote;

    public Quote(int mImage, int mQuote) {
        this.mImage = mImage;
        this.mQuote = mQuote;
    }

    public int getmImage() {
        return mImage;
    }

    public int getmQuote() {
        return mQuote;
    }
}
